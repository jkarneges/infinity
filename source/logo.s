 	ldh (P_BCPS),a
 	ld b,#8
in1: 	
 	ld a,(hli)
 	ldh (P_BCPD),a
 	dec b
 	jr nz,in1
	ret

_showlogo::
	di	
	call linewait

	xor a
	ldh (P_SCX),a
	ldh (P_SCY),a
	ld  a, #0b10010001
 	ldh (P_LCDC),a
 
	;clear colors
	ld a,#128
 	ldh (P_BCPS),a
 	ld b,#64
 	xor a
clr: 	
 	ldh (P_BCPD),a
 	dec b
 	jr nz,clr

 	;clear attrmap
	ld a,#1
  	LDH   (P_VBK), A 
  	ld hl,#C_PA_VRAM_PT
  	xor a
  	call _STIR_VRAM

  	; copy tiles and map
 	xor a
   	ldh (P_VBK),a 

   	ld de,#tiles
   	ld hl,#C_PA_VRAM_PT
   	ld bc,#tiles_size
   	call _LDIR_VRAM_ACC

   	ld de,#map
   	ld hl,#C_PA_VRAM_NT
   	ld bc,#map_size
   	call _LDIR_VRAM_ACC

	ld a,#30
	ld hl, #paldata
finloop:	
	push af
	call linewait
	call setpal
	pop af
	dec a
	jr nz,finloop

	ld b,#60
lowo:
	call linewait
	dec b
	jr nz,lowo

	ld a,#30
	ld hl, #palend
	ld de,#0xfff8
	add hl,de
	ld e,#0xf0
foutloop:	
	push af
	call linewait
	call setpal
	add hl,de
	pop af
	dec a
	jr nz,foutloop	

	ei
	ret

	

